package pdkel;
public class Multidim {
    public static void main(String[]args){
        String[][] nama = {
            {"Pak", "Bu", "Mbak"},
            {"Joko", "Susi"}
        };
        
        System.out.println(nama[0][1]+ nama[1][0]);
        System.out.println(nama[0][1]+ nama[1][1]);
        System.out.println(nama[0][2]+ nama[1][0]);
        
        
    }
}
