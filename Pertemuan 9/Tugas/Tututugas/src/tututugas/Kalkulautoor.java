    private void SamadenganBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SamadenganBTActionPerformed
       if (!operator.isEmpty() && !AngkaTF.getText().isEmpty()) {
            double bilangan2 = Double.parseDouble(AngkaTF.getText());
            double hasil = 0;
            
            switch (operator) {
                case "+": hasil = bilangan1 + bilangan2; break;
                case "-": hasil = bilangan1 - bilangan2; break;
                case "*": hasil = bilangan1 * bilangan2; break;
                case "/": hasil = (bilangan2 != 0) ? (bilangan1 / bilangan2) : 0; break;
                case "%": hasil = bilangan1 % bilangan2; break;
            }
            
            if (hasil % 1 == 0) {
                AngkaTF.setText(String.valueOf((int) hasil));
            } else {
                AngkaTF.setText(String.valueOf(hasil));
            }
            operator = "";
            siapInputBaru = true;
        }
    }//GEN-LAST:event_SamadenganBTActionPerformed

