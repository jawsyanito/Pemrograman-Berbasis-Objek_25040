    private void tambahAngka(String angka) {
        if (siapInputBaru) {
            AngkaTF.setText(angka);
            siapInputBaru = false;
        } else {
            AngkaTF.setText(AngkaTF.getText() + angka);
        }
    }
    
    private void pilihOperator(String op) {
        if (!AngkaTF.getText().isEmpty()) {
            bilangan1 = Double.parseDouble(AngkaTF.getText());
            operator = op;
            siapInputBaru = true;
        }
    }

